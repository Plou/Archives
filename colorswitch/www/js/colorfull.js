var Colorfull;

Colorfull = (function() {
  function Colorfull(selector) {
    this.body = angular.element(selector);
    this.colors = colors
    return this;
  }

  Colorfull.prototype.getRandomColor = function() {
    return [Math.floor(Math.random() * this.colors.length)];
  };

  Colorfull.prototype.updateColor = function(color, duration, easing) {
    var transition;
    if (easing == null) {
      easing = 'ease-in-out';
    }
    if (duration == null) {
      duration = 1;
    }
    transition = 'background ' + duration + 's ' + easing;
    this.body.css('transition', transition);
    if (color == null) {
      color = this.colors[this.getRandomColor()];
    }
    this.body.css('backgroundColor', color);
    return this;
  };

  return Colorfull;

})();
